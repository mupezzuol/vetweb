package com.vetweb.model.report;

public class ClientesDevedoresVO {
	
	private String nome;
	
	private String debitoTotal;
	
	private String valoresPorTipoOcorrencia;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getDebitoTotal() {
		return debitoTotal;
	}

	public void setDebitoTotal(String debitoTotal) {
		this.debitoTotal = debitoTotal;
	}

	public String getValoresPorTipoOcorrencia() {
		return valoresPorTipoOcorrencia;
	}

	public void setValoresPorTipoOcorrencia(String valoresPorTipoOcorrencia) {
		this.valoresPorTipoOcorrencia = valoresPorTipoOcorrencia;
	}
	
}
